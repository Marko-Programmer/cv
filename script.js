document.addEventListener("DOMContentLoaded", function () {
    const langButtons = document.querySelectorAll(".lang_btn");

    let currentLang = localStorage.getItem("lang") || "en";
    applyLanguage(currentLang);

    langButtons.forEach(button => {
        button.addEventListener("click", function () {
            const selectedLang = this.getAttribute("data-lang");
            if (currentLang !== selectedLang) {
                currentLang = selectedLang;
                localStorage.setItem("lang", currentLang);
                applyLanguage(currentLang);
            }
        });
    });

    function applyLanguage(lang) {
        langButtons.forEach(btn => btn.classList.remove("active"));
        document.querySelector(`.lang_btn[data-lang="${lang}"]`).classList.add("active");

        document.querySelectorAll("[data-i18n]").forEach(element => {
            const key = element.getAttribute("data-i18n");
            if (translations[lang] && translations[lang][key]) {
                element.innerHTML = translations[lang][key];
            }
        });

        const downloadLnk = document.getElementById("download_cv");
        if(downloadLnk) {
            downloadLnk.href = translations[lang].cv_link;
            downloadLnk.download = translations[lang].cv_name;
        }
    }
});